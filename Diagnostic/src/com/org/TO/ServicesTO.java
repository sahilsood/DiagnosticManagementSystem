package com.org.TO;

public class ServicesTO {
	private int serviceId;
    private String serviceName;
    private String serviceFeatures;
    private String serviceBenefits;
    private String serviceParameters;
	public String getServiceFeatures() {
		return serviceFeatures;
	}
	public void setServiceFeatures(String serviceFeatures) {
		this.serviceFeatures = serviceFeatures;
	}
	public String getServiceBenefits() {
		return serviceBenefits;
	}
	public void setServiceBenefits(String serviceBenefits) {
		this.serviceBenefits = serviceBenefits;
	}
	public String getServiceParameters() {
		return serviceParameters;
	}
	public void setServiceParameters(String serviceParameters) {
		this.serviceParameters = serviceParameters;
	}
	public int getServiceId() {
		return serviceId;
	}
	public void setServiceId(int serviceId) {
		this.serviceId = serviceId;
	}
	public String getServiceName() {
		return serviceName;
	}
	public void setServiceName(String serviceName) {
		this.serviceName = serviceName;
	}
    
}
